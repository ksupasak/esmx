  def get_document name
     instance = get_instance
     instance[:documents].each do |i|
       if i.name == name
         doc = Document.find(i.id)
         doc.project = self
        return doc
       end
       
     end
    
    return nil
  end

  def get_service name
     instance = get_instance
     instance[:services].each do |i|
       if i.name == name
         doc = Service.find(i.id)
         doc.project = self
        return doc
       end
       
     end
    
    return nil
  end
  
