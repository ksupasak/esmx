    def xml_attr name, ele
      ele.elements.each("#{name}") { |element| self[name] = element.text }
    end


    def import ele , force=true
           xml_attr :name ,ele
           xml_attr :package ,ele
           xml_attr :extended ,ele
           xml_attr :domain ,ele
           xml_attr :title ,ele
           self.save

        if force
            self.services.destroy_all
            ele.elements.each("services/service") do |element| 
              s = self.services.new
              s.import element
            end
         end

    end
    
    def update_settings params
      settings = get_instance[:settings]
      hash = {}
      for i in settings
        hash[i.name]=i
      end
      
      params.each_pair do |k,v|
          obj = hash[k]
          if obj.project_id == self.id
            obj.update_attributes :value=>v
          else
            if v!=obj.value
              self.settings.create :name=>k,:value=>v
            end
          end
      end

    end
    
    def setting name
       settings = get_instance[:settings]
       hash = {}
       for i in settings
         hash[i.name]=i
       end
       if hash[name]
       return hash[name].value 
       else
       return nil
       end
    end
    def get_setting name
        setting name
    end
   
   
  
